import mongoose, { Schema } from 'mongoose'

const detailSchema = new Schema({
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'user',
    required: true
  },
  title: {
    type: String,
    required: true,
    trim: true
  },
  completed: {
    type: Boolean,
    default: false
  }
}, {
  timestamps: true
})
export const DetailModel = mongoose.model('details', detailSchema)
